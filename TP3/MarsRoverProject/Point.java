package MarsRoverProject;

public class Point {
    private int xcoord;
    private int ycoord;

    public Point(int x, int y) {
        this.xcoord = x;
        this.ycoord = y;
    }

    public int getXcoord() {
        return xcoord;
    }

    public int getYcoord() {
        return ycoord;
    }
}
